// Capture the original English content straight from the DOM. The <title> is
// a data-i18n element too, so the page title is localized like everything else.
const english = {};
document.querySelectorAll('[data-i18n]').forEach(function (el) {
  english[el.dataset.i18n] = el.innerHTML;
});

function applyLanguage(lang) {
  const dict = lang === 'en' ? english : Object.assign({}, english, translations[lang]);

  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    const key = el.dataset.i18n;
    if (dict[key] != null) el.innerHTML = dict[key];
  });

  // Swap language-specific images (e.g. assets/images/<lang>/hero.webp).
  document.querySelectorAll('[data-i18n-img]').forEach(function (el) {
    el.src = el.src.replace(/assets\/images\/[a-z]{2}\//, 'assets/images/' + lang + '/');
  });

  document.documentElement.lang = lang;

  renderFaq(lang);
  renderLegal(lang);
}

// Build a legal document (Privacy Policy or Terms of Service) from
// assets/js/legal-data.js into the [data-legal] container of the current page.
// Called on every language change so the text stays in the active language.
// No-op on pages without a [data-legal] container.
function renderLegal(lang) {
  const host = document.querySelector('[data-legal]');
  const data = window.LEGAL_CONTENT;
  if (!host || !data) return;

  const doc = data[host.dataset.legal];
  if (!doc) return;

  const pick = function (entry) { return entry[lang] || entry.en; };

  let html = '<p class="legal__lead">' + pick(doc.meta.updated) + '</p>' +
    '<div class="legal__note">' + pick(doc.meta.note) + '</div>';

  html += doc.sections.map(function (section) {
    const content = pick(section);
    return '<section class="legal__section">' +
      '<h2 class="legal__subhead">' + content.head + '</h2>' +
      content.body +
      '</section>';
  }).join('');

  host.innerHTML = html;
}

// Build the FAQ list from assets/js/faq-data.js for the given language. Called
// on every language change so the questions stay in the active language.
function renderFaq(lang) {
  const data = window.FAQ_CONTENT;
  const list = document.querySelector('.faq__list');
  if (!data || !list) return;

  const headline = document.querySelector('.faq__headline');
  if (headline && data.headline) {
    headline.textContent = data.headline[lang] || data.headline.en;
  }

  const chevron = '<svg class="faq__chevron" width="20" height="20" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg" aria-hidden="true"><path d="M6 9l6 6 6-6" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"/></svg>';

  list.innerHTML = data.items.map(function (item, i) {
    const t = item[lang] || item.en;
    const open = i === 0;
    return '<div class="faq__item' + (open ? ' is-open' : '') + '">' +
      '<button class="faq__question" type="button" aria-expanded="' + (open ? 'true' : 'false') + '">' +
      '<span>' + t.q + '</span>' + chevron +
      '</button>' +
      '<div class="faq__answer"><p>' + t.a + '</p></div>' +
      '</div>';
  }).join('');
}

// Language switcher dropdown
(function () {
  const switcher = document.querySelector('.lang-switch');
  if (!switcher) return;

  const toggle = switcher.querySelector('.lang-switch__toggle');
  const flag = switcher.querySelector('.lang-switch__flag');
  const label = switcher.querySelector('.lang-switch__label');
  const items = switcher.querySelectorAll('.lang-menu__item');

  function open() {
    switcher.classList.add('is-open');
    toggle.setAttribute('aria-expanded', 'true');
  }

  function close() {
    switcher.classList.remove('is-open');
    toggle.setAttribute('aria-expanded', 'false');
  }

  function selectLang(lang, updateUrl) {
    items.forEach(function (i) {
      const active = i.dataset.lang === lang;
      i.classList.toggle('is-active', active);
      if (active) {
        flag.src = i.dataset.flag;
        label.textContent = i.dataset.label;
      }
    });
    // Keep the in-modal cookie-consent language toggle in sync, if present.
    document.querySelectorAll('[data-cc-lang]').forEach(function (b) {
      const active = b.dataset.ccLang === lang;
      b.classList.toggle('is-active', active);
      b.setAttribute('aria-pressed', active ? 'true' : 'false');
    });
    applyLanguage(lang);
    try { localStorage.setItem('lang', lang); } catch (e) {}

    if (updateUrl) {
      const url = new URL(window.location.href);
      url.searchParams.set('lang', lang);
      // Browsers block URL rewriting on file:// pages, so guard against it.
      try { history.replaceState(null, '', url); } catch (e) {}
    }
  }

  toggle.addEventListener('click', function (e) {
    e.stopPropagation();
    switcher.classList.contains('is-open') ? close() : open();
  });

  items.forEach(function (item) {
    item.addEventListener('click', function () {
      selectLang(item.dataset.lang, true);
      close();
    });
  });

  // The cookie-consent modal carries its own EN/DE toggle (the modal covers
  // the header switcher). Route its clicks through the same selectLang.
  document.addEventListener('click', function (e) {
    const langBtn = e.target.closest('[data-cc-lang]');
    if (!langBtn) return;
    selectLang(langBtn.dataset.ccLang, true);
  });

  // Close when clicking outside
  document.addEventListener('click', function (e) {
    if (!switcher.contains(e.target)) close();
  });

  // Close on Escape
  document.addEventListener('keydown', function (e) {
    if (e.key === 'Escape') close();
  });

  // Pick the initial language: ?lang= in the URL wins, then the saved choice,
  // then the browser's preferred language (so a German visitor sees the German
  // site, and the cookie consent popup, on first load without having to reach
  // the language switcher, which the consent modal covers). English is the
  // final fallback.
  const supported = Array.from(items).map(function (i) { return i.dataset.lang; });
  const urlLang = new URL(window.location.href).searchParams.get('lang');
  let saved;
  try { saved = localStorage.getItem('lang'); } catch (e) {}

  const browserLangs = navigator.languages || [navigator.language || ''];
  const browserLang = browserLangs
    .map(function (l) { return String(l).slice(0, 2).toLowerCase(); })
    .filter(function (l) { return supported.indexOf(l) !== -1; })[0];

  const initial = supported.indexOf(urlLang) !== -1 ? urlLang
    : supported.indexOf(saved) !== -1 ? saved
    : browserLang || 'en';
  // Sync the URL on load so it always reflects the active language.
  selectLang(initial, true);
})();

// FAQ accordion — clicking a question opens it and closes the others. Uses
// event delegation so it keeps working after the list is re-rendered on a
// language change.
(function () {
  const list = document.querySelector('.faq__list');
  if (!list) return;

  list.addEventListener('click', function (e) {
    const question = e.target.closest('.faq__question');
    if (!question || !list.contains(question)) return;

    const item = question.closest('.faq__item');
    const willOpen = !item.classList.contains('is-open');

    list.querySelectorAll('.faq__item').forEach(function (other) {
      other.classList.remove('is-open');
      other.querySelector('.faq__question').setAttribute('aria-expanded', 'false');
    });

    if (willOpen) {
      item.classList.add('is-open');
      question.setAttribute('aria-expanded', 'true');
    }
  });
})();

// Store badges — fill every App Store / Google Play link from the single
// source of truth in config.js. Badges are matched by their aria-label, so
// changing a URL only ever needs one edit (in assets/js/config.js).
(function () {
  const links = window.NEOMIND_LINKS;
  if (!links) return;

  document.querySelectorAll('a[aria-label="Download on the App Store"]').forEach(function (a) {
    a.href = links.appStore;
  });
  document.querySelectorAll('a[aria-label="Get it on Google Play"]').forEach(function (a) {
    a.href = links.playStore;
  });
})();

// QR widget — dismiss button hides it for the rest of the session.
(function () {
  const widget = document.querySelector('.qr-widget');
  if (!widget) return;

  const close = widget.querySelector('.qr-widget__close');
  if (close) {
    close.addEventListener('click', function () {
      widget.classList.add('is-dismissed');
    });
  }
})();

// Download modal — opened by the header "Try for free" button.
(function () {
  const modal = document.querySelector('.modal');
  const trigger = document.querySelector('.btn-try');
  if (!modal || !trigger) return;

  const closeBtn = modal.querySelector('.modal__close');

  function open() {
    modal.hidden = false;
    // Force reflow so the opening transition runs from the hidden state.
    void modal.offsetWidth;
    modal.classList.add('is-open');
    document.body.style.overflow = 'hidden';
  }

  function close() {
    modal.classList.remove('is-open');
    document.body.style.overflow = '';
    setTimeout(function () { modal.hidden = true; }, 200);
  }

  trigger.addEventListener('click', open);
  closeBtn.addEventListener('click', close);

  // Click on the dimmed backdrop (outside the dialog) closes it.
  modal.addEventListener('click', function (e) {
    if (e.target === modal) close();
  });

  document.addEventListener('keydown', function (e) {
    if (e.key === 'Escape' && !modal.hidden) close();
  });
})();
